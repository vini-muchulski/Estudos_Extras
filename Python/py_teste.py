from datetime import date

class Funcionario:
    def __init__(self,nome,dataNascimento, salario):
      self._nome = nome
      self._dataNascimento = dataNascimento
      self._salario = salario

    @property
    def nome(self):
      return self._nome

    @property
    def salario(self):
      return self._salario

    def idade(self):
      lista_dataNasc = self._dataNascimento.split("/")
      ano = lista_dataNasc[-1]
     

      ano_atual = date.today().year

      idade = ano_atual - int(ano)

      return idade





    def calcular_bonus(self):
      valor = self._salario*0.1
      if valor >1000:
        valor = 0
      
      return valor

    
    def __str__(self):
      return f" Funcionario {self._nome} - Nascimento {self._dataNascimento} - Sal√°rio {self._salario}"

